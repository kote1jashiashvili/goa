
function person(name, last_name, phone_number, email, password, confirm_password){
    this.name = name
    this.last_name = last_name
    this.phone_number = phone_number
    this.email = email
    this.password = password
    this.confirm_password = confirm_password

    console.log("my name is " + name + " my last name is " + last_name + " my phone number is " + phone_number + " my email is " + email + "my password is " + password + " and my confirmed password is " + confirm_password + "." )
}


